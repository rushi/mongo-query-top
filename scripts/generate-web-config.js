#!/usr/bin/env node

/**
 * Generate web frontend configuration from config YAML files
 * This eliminates the need for environment variables by reading from config/local.yaml
 */

import { writeFileSync } from "fs";
import config from "config";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const rootDir = join(__dirname, "..");

// Read config using the config module (handles merging default + local automatically)
const apiPort = config.get("api.port");
const apiKey = config.get("api.apiKey");

// Allow override via command-line argument for API URL (for production builds)
const apiUrl = process.argv[2] || `http://localhost:${apiPort}`;

// Generate TypeScript config file
const configContent = `/**
 * Application configuration
 * Auto-generated from config/local.yaml by scripts/generate-web-config.js
 * DO NOT EDIT THIS FILE MANUALLY
 */

interface AppConfig {
    apiUrl: string;
    apiKey: string;
}

const config: AppConfig = {
    apiUrl: "${apiUrl}",
    apiKey: "${apiKey}",
};

export default config;
`;

// Write config file
const outputPath = join(rootDir, "apps", "web", "src", "config.ts");
writeFileSync(outputPath, configContent);

console.log("✅ Generated web config from YAML:");
console.log(`   API URL: ${apiUrl}`);
console.log(`   API Key: ${apiKey.substring(0, 10)}...`);
console.log(`   Output: ${outputPath}`);
